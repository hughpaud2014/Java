// Compute Area of a circle
// Call file "AreaApp.java"


public class AreaApp {
    public static void main(String args[]) {
        double pi, r, a;

        r = 10.8; // radius of a circle
        pi = 3.1416; // pi, approximately
        a = pi * r * r;

        System.out.println("Area of a circle is " + a);


    }

    
}
